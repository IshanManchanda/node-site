<script>
export default {
    name: 'alphanode',
    data: function(){
        return{
            navColor: 'transparent-light',
            footerColor: 'dark'
        }
    },
    mounted() {
        this.$emit('emit-nav-color', this.navColor);
        this.$emit('emit-footer-color', this.footerColor);

        window.addEventListener('scroll', this.onscrollHandler);
    },
    methods:{
        onscrollHandler: function(){
            if(window.scrollY != 0){
                this.navColor = 'light';
                this.$emit('emit-nav-color', this.navColor);
            }else{
                this.navColor = 'transparent-light';
                this.$emit('emit-nav-color', this.navColor);
            }
        },
        timer: function(){
            // Set the date we're counting down to
            let countDownDate = new Date("July 10, 2020 00:00:00").getTime();

            // Update the count down every 1 second
            let x = setInterval(function() {
                // Get todays date and time
                let now = new Date().getTime();

                // Find the time between now and the count down date
                let diff = countDownDate - now;

                // Time calculations for days, hours, minutes and seconds
                let days = Math.floor(diff / (1000 * 60 * 60 * 24));
                let hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
                let minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
                let seconds = Math.floor((diff % (1000 * 60)) / 1000);

                // Display the result in the element with id="demo"
                document.getElementById("timer").innerHTML = days + "d " + hours + "h "
                + minutes + "m " + seconds + "s ";

                // If the count down is finished, write some text 
                if (diff <= 0) {
                    clearInterval(x);
                    document.getElementById("timer").innerHTML = "EXPIRED";
                }
            }, 1000);
        }
    }
}
</script>
